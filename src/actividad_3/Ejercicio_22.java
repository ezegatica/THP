package actividad_3;

public class Ejercicio_22 {
/*
 * Realizá un programa que muestre todos los números enteros del 
 * 1 al 5, y luego los mismos números pero en orden inverso. [EC]
 */
	public static void main(String[] args) {
		for(int i = 1; i <= 5; i++) {
			System.out.println(i);
		}
		
		for(int i = 5; i >= 1; i--) {
			System.out.println(i);
		}
	}

}
